export function t(key, lang = 'en-IN') {
  if (!key) return "";
  let safeKey = key;
  if (key.includes(" ")) {
    safeKey = key.split(' ').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('');
  }
  const dictionary = translations[safeKey] || translations[key];
  if (!dictionary) return key; 
  return dictionary[lang] || dictionary["en-IN"] || key;
}
