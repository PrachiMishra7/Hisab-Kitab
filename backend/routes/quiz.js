// ── GET /api/quiz/generate ─────────────────────────────────────
router.get('/generate', (req, res) => {
  const lang  = req.query.lang  || 'en';
  const level = parseInt(req.query.level) || 1;

  let templates = allTemplates.filter(t => t.level === level);
  if (templates.length === 0) templates = allTemplates;

  const generated = [];
  const used = new Set();

  for (let i = 0; i < Math.min(5, templates.length * 3); i++) {
    // Try to pick a unique template
    let tpl;
    let attempts = 0;
    do { tpl = pick(templates); attempts++; } while (used.has(tpl) && attempts < 10);
    if (attempts < 10) used.add(tpl);

    const n  = pick(names[lang]  || names.en);
    const b  = pick(banks[lang]  || banks.en);
    const a  = pick(amounts);
    const ap = pick(apps);

    const rv = (str) => (str || '')
      .replace(/{name}/g, n)
      .replace(/{bank}/g, b)
      .replace(/{amount}/g, a)
      .replace(/{app}/g, ap);

    // Build options: 1 correct + 3 incorrect (or as many as available)
    const incorrectPool = tpl.incorrect || [];
    const wrongOpts = incorrectPool.map((inc, idx) => ({
      id: `opt_w_${i}_${idx}`,
      text: rv(inc[lang] || inc.en),
      isCorrect: false,
      explanation: rv(tpl.explanation[lang] || tpl.explanation.en),
    }));

    const correctOpt = {
      id: `opt_c_${i}`,
      text: rv(tpl.correct[lang] || tpl.correct.en),
      isCorrect: true,
      explanation: rv(tpl.explanation[lang] || tpl.explanation.en),
    };

    const allOpts = [correctOpt, ...wrongOpts].sort(() => 0.5 - Math.random());

    generated.push({
      id: `gen_${Date.now()}_${i}`,
      category: tpl.category[lang] || tpl.category.en,
      question:  rv(tpl.template[lang]  || tpl.template.en),
      options:   allOpts,
    });

    if (generated.length === 5) break;
  }

  res.json(generated);
});

